// Write a program of C++ to find square & cube of given number

#include <iostream>
using namespace std;

int main () {
    int a;

    cout << "enter any number: ";
    cin >> a;

    cout << "square of " << a << " is " << a*a << endl;
    cout << "cube of " << a << " is " << a*a*a;
    
    return 0;
}